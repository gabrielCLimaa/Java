    /* Load the data base */
    private void loadData() {
         ArrayList<Cliente> clientes = new ArrayList<>();   
         /* Create Ccnnection with data base */
         Bd bd = new Bd();
         try {
            PreparedStatement stmt = bd.getCon().prepareStatement("SELECT "
                    + "* FROM Cliente WHERE Cliente.status = 1");
            
            ResultSet result = stmt.executeQuery();

            while(result.next()) {
                clientes.add(new Cliente(
                    result.getInt("id"),
                    result.getString("clientName"),
                    result.getString("address"),
                    result.getDate("date"),
                    result.getInt("status")
                ));
            }
            
            DefaultTableModel dataClientModel = (DefaultTableModel) jTable3.getModel();
            
            // ZERAR TABELA
            dataClientModel.setRowCount(0);
            
            // INSERIR NA TABELA
            clientes.forEach((cliente) -> {
                Object[] row = {
                    cliente.getId(),
                    cliente.getClientNome(),
                    cliente.getAddress(),
                    cliente.getDate(),
                    cliente.getStatus()
                };
                
                dataClientModel.addRow(row);
            });
            
            bd.closeCon();
        } catch(SQLException err) {
            System.out.println(err.getMessage());
        }
    }
    
